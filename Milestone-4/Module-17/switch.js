var color = 'red';

switch(color){
    case 'green':
        console.log("Color is green");
        break;
    case 'yellow':
        console.log("color is yellow");
        break;
    case 'blue':
        console.log("color is blue");
        break;
    case 'alpha':
        console.log("color is alpha");
        break;
    case 'red':
        console.log("color is red");
        break;
    default:
        console.log("no match");

}